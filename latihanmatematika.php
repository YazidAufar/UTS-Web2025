<?php
    $angka1 = 0.5; 
    $angka2 = 1.5; 


    // Penjumlahan
    $penjumlahan = $angka1 + $angka2;
    echo "<h2>Penjumlahan</h2>";
    echo "<p>$angka1 + $angka2 = $penjumlahan</p>";
    // Pengurangan
    $pengurangan = $angka1 - $angka2;
    echo "<h2>Pengurangan</h2>";
    echo "<p>$angka1 - $angka2 = $pengurangan</p>";
    // Perkalian
    $perkalian = $angka1 * $angka2;
    echo "<h2>Perkalian</h2>";
    echo "<p>$angka1 * $angka2 = $perkalian</p>";
    // Pembagian
    $pembagian = $angka1 / $angka2;
    echo "<h2>Pembagian</h2>";
    echo "<p>$angka1 / $angka2 = $pembagian</p>";

?>